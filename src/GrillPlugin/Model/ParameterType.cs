namespace GrillPlugin.Model
{
    /// <summary>
    /// Названия параметров мангала.
    /// </summary>
    public enum ParameterType
    {
        /// <summary>
        /// Высота короба мангала.
        /// </summary>
        BoxHeight,

        /// <summary>
        /// Длина короба мангала.
        /// </summary>
        BoxLength,

        /// <summary>
        /// Ширина короба мангала.
        /// </summary>
        BoxWidth,

        /// <summary>
        /// Толщина стен мангала.
        /// </summary>
        BoxWallThickness,

        /// <summary>
        /// Диаметр ямки для шампура.
        /// </summary>
        GrooveDiameter,

        /// <summary>
        /// Расстояние между ямками для шампуров.
        /// </summary>
        GrooveDistance,

        /// <summary>
        /// Диаметр отверстия для воздуха.
        /// </summary>
        HoleDiameter,

        /// <summary>
        /// Расстояние между отверстиями для воздуха.
        /// </summary>
        HoleDistance,

        /// <summary>
        /// Высота цетра отверстия для воздуха.
        /// </summary>
        HoleHeight,

        /// <summary>
        /// Диаметр ножки мангала.
        /// </summary>
        LegDiameter,

        /// <summary>
        /// Высота ножки мангала.
        /// </summary>
        LegHeight
    }
}
