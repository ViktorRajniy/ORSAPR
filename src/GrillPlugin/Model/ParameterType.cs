namespace ModelData
{
    /// <summary>
    /// Названия параметров мангала.
    /// </summary>
    public enum ParameterType
    {
        /// <summary>
        /// Высота короба мангала.
        /// </summary>
        BoxHeight,

        /// <summary>
        /// Длина короба мангала.
        /// </summary>
        BoxLength,

        /// <summary>
        /// Ширина короба мангала.
        /// </summary>
        BoxWidth,

        /// <summary>
        /// Толщина стен мангала.
        /// </summary>
        BoxWallThickness,

        /// <summary>
        /// Диаметр круголого паза для шампура.
        /// </summary>
        CircleGrooveDiameter,

        /// <summary>
        /// Расстояние между пазами для шампуров.
        /// </summary>
        CircleGrooveDistance,

        /// <summary>
        /// Расстояние между прямоугольными пазами для шампуров.
        /// </summary>
        RectangleGrooveWidth,

        /// <summary>
        /// Глубина прямоугольных пазов для шампуров.
        /// </summary>
        RectangleGrooveHeight,

        /// <summary>
        /// Глубина прямоугольных пазов для шампуров.
        /// </summary>
        RectangleGrooveDistance,

        /// <summary>
        /// Диаметр круглого отверстия для воздуха.
        /// </summary>
        CircleHoleDiameter,

        /// <summary>
        /// Расстояние между отверстиями для воздуха.
        /// </summary>
        CircleHoleDistance,

        /// <summary>
        /// Высота цетра круглого отверстия для воздуха.
        /// </summary>
        CircleHoleHeight,

        /// <summary>
        /// Высота прямоугольного выреза для воздуха.
        /// </summary>
        RectangleHoleHeight,

        /// <summary>
        /// Диаметр ножки мангала.
        /// </summary>
        LegDiameter,

        /// <summary>
        /// Высота ножки мангала.
        /// </summary>
        LegHeight
    }
}
